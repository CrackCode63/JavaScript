// The expression is a simple arithmetic expression with one variable, x. The operators are +, -, \*, /, and ^ (exponentiation).
let a = 2;
let b = 3;
console.log("a + b", a+b);
console.log("a + b", a-b);
console.log("a + b", a*b);
console.log("a + b", a**b);
console.log("a + b", a/b);
console.log("a + b", a%b);

