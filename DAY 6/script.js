// > (greater than)
// < (less than)
// >= (greater than or equal to)
// <= (less than or equal to)

// Strict equality and inequality are used to check if two values are the same or not. They are used to check if two values are the same type and value.
// For example, 1 == "1" is true because the string "1" can be converted to the number 1. But 1 === "1" is false because the string
// "1" is not the same type as the number 1.

